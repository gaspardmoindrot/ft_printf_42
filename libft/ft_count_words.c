#include "libft.h"

int		ft_count_words(char *s)
{
	int		n;
	int		i;

	i = 0;
	n = 1;
	while (s[i])
	{
		if ((s[i] == ' ' || s[i] == '\n' || s[i] == '\t') && s[i + 1] != ' '
				&& s[i + 1] != '\n' && s[i + 1] != '\t' && s[i + 1] != '\0')
			n++;
		i++;
	}
	return (n);
}
